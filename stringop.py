st = "python is fun programming language."
print(st)
# capitalize the sentence
print(st.capitalize())
# capitalize each word
print(st.title())
# change to all caps
st = st.upper()
print(st)
# change to all low
st = st.lower()
print(st)
# split the sentences using common literal
stlist = st.split(" ")
print(stlist)
# to replace something in the string
st = st.replace("fun", "funny")
print(st)
